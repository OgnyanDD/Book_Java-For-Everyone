package Chapter_6;

public class Example_02 {
    public static void main(String[] args) {
        int[] totoNumbers = {2, 23, 12, 42, 14, 5};
        for (int index = 0; index <= totoNumbers.length - 1; index++) {
            System.out.print(totoNumbers[index] + " "); // 2 23 12 42 14 5
        }
    }
}
